# Glosarry

glossary = {
    'list'  : 'A data type that can store a collection of values',
    'statement' : 'a line of code that performs a specific task',
    'variable' : 'use to store data',
    'function' : ' a block of code that performs a specific task',
    'argument' : ' a value that is given to a function when it is called'
}
 
for word , meaning in glossary.items():
 print(f"{word}: {meaning}\n")

 
